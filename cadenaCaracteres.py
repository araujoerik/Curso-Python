#Las cadenas son una secuencia de caracteres, usados para representar texto

lenguaje = "Python"

print('Comenzando a aprender ' + lenguaje)
longitud = len(lenguaje)    # Largo de la cadena
print(longitud)             #Imprimiendo la longitud de la cadena

#Imprimiendo por indice de cadena

print(lenguaje[0])  #P
print(lenguaje[1])  #y
print(lenguaje[2])  #t
print(lenguaje[3])  #h
print(lenguaje[4])  #o
print(lenguaje[5])  #n

# Mayusculas
print(lenguaje.upper())

# Minusculas
print(lenguaje.lower())
